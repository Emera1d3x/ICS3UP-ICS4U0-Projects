document.addEventListener('DOMContentLoaded', function() {
    var collapsibleBtn = document.querySelector('.collapsible-btn');
    var collapsibleContent = document.querySelector('.collapsiblecontent');

    collapsibleBtn.addEventListener('click', function() {
        // Toggle the visibility of the content
        collapsibleContent.style.display = (collapsibleContent.style.display === 'none') ? 'block' : 'none';
    });
});

